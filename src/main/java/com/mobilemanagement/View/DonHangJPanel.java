    DonHang getForm() {
        DonHang dh = new DonHang();

        // Kiểm tra và thiết lập MaDH
        String maDH = txtMaDH.getText().trim();
        if (maDH.isEmpty()) {
            MsgBox.alert(this, "Lỗi: Chưa nhập MaDH");
            return null;
        }
        dh.setMaDH(maDH);

        // Thiết lập NgayLap
        java.util.Date currentDate = new java.util.Date();

        // Chuyển đổi ngày thành định dạng java.sql.Date và đặt giá trị cho NgayLap
        dh.setNgayLap(new java.sql.Date(currentDate.getTime()));

        // Kiểm tra xem txtNgayLap có được khởi tạo hay không
        if (txtNgayLap != null && !txtNgayLap.getText().isEmpty()) {
            try {
                dh.setNgayLap(java.sql.Date.valueOf(txtNgayLap.getText()));
            } catch (Exception e) {
                // Xử lý khi chuỗi ngày không đúng định dạng
                //MsgBox.alert(this, "Lỗi: Chuỗi ngày không đúng định dạng.");
            }
        }

        // Thiết lập TrangThai
        String trangThai = (String) cboTrangThai.getSelectedItem();
        if (trangThai == null || trangThai.isEmpty()) {
            MsgBox.alert(this, "Lỗi: Chưa chọn Trạng Thái");
            return null;
        }
        dh.setTrangThai(trangThai);

        // Thiết lập GhiChu
        dh.setGhiChu(txtGhiChu.getText());

        // Thiết lập MaKH
        if (!setIntValue(txtMaKH, "MaKH", dh::setMaKH)) {
            return null;
        }

        // Thiết lập MaTK
        if (!setIntValue(txtMaTK, "Mã TK", dh::setMaTK)) {
            return null;
        }

        // Thiết lập MaGG
        dh.setMaGG(txtGiamGia.getText());

        return dh;
    }

// Phương thức tiện ích để kiểm tra và thiết lập giá trị số nguyên từ JTextField
    private boolean setIntValue(JTextField textField, String fieldName, Consumer<Integer> setter) {
        String text = textField.getText().trim();
        if (text.isEmpty()) {
            MsgBox.alert(this, "Lỗi: Chưa nhập " + fieldName);
            return false;
        }

        try {
            int value = Integer.parseInt(text);
            setter.accept(value);
            return true;
        } catch (NumberFormatException e) {
            MsgBox.alert(this, "Lỗi: Sai định dạng " + fieldName + ", Nhập Lại!");
            return false;
        }
    }

    DonHangCT getFormct() {
    DonHangCT dhct = new DonHangCT();

    // Kiểm tra và chuyển đổi mã đơn hàng từ chuỗi sang số nguyên
    String maDHText = txtMadh.getText();
    if (maDHText.isEmpty()) {
        // Hiển thị thông báo lỗi nếu mã đơn hàng trống
        MsgBox.alert(this, "Không để mã đơn hàng trống!");
        return null;
    }
    dhct.setMaDH(maDHText);

    // Kiểm tra và chuyển đổi mã sản phẩm từ chuỗi sang số nguyên
    String maSPText = (String) cboMasp.getSelectedItem();
    if (maSPText == null || maSPText.isEmpty()) {
        // Hiển thị thông báo lỗi nếu mã sản phẩm trống
        MsgBox.alert(this, "Không để mã Sản phẩm trống!");
        return null;
    }
    dhct.setMaSP(maSPText);

    // Kiểm tra và chuyển đổi số lượng từ chuỗi sang số nguyên
    String soLuongText = txtSL.getText();
    if (soLuongText.isEmpty()) {
        // Hiển thị thông báo lỗi nếu số lượng trống
        MsgBox.alert(this, "Không để Số Lượng trống!");
        return null;
    }
    try {
        dhct.setSoLuong(Integer.parseInt(soLuongText));
    } catch (NumberFormatException e) {
        // Hiển thị thông báo lỗi nếu số lượng không phải là số nguyên hợp lệ
        MsgBox.alert(this, "Số lượng không phải số nguyên hợp lệ!");
        return null;
    }

    // Kiểm tra và chuyển đổi giá đã giảm từ chuỗi sang số thực
    String giaDaGiamText = txtGiaDaGiam.getText();
    if (giaDaGiamText.isEmpty()) {
        // Hiển thị thông báo lỗi nếu giá đã giảm trống
        MsgBox.alert(this, "Không Giá tiền trống!");
        return null;
    }
    try {
        dhct.setGiaDaGiam(Double.parseDouble(giaDaGiamText));
    } catch (NumberFormatException e) {
        // Hiển thị thông báo lỗi nếu giá đã giảm không phải là số thực hợp lệ
        MsgBox.alert(this, "Giá tiền không hợp lệ!");
        return null;
    }

    // Chuyển đổi tổng giá từ chuỗi sang số thực
    String tongGiaText = txtTongGia.getText();
    if (tongGiaText.isEmpty()) {
        dhct.setTongGia(0.0); // Gán giá trị mặc định nếu tổng giá trống
    } else {
        try {
            dhct.setTongGia(Double.parseDouble(tongGiaText));
        } catch (NumberFormatException e) {
            // Hiển thị thông báo lỗi nếu tổng giá không phải là số thực hợp lệ
            
            return null;
        }
    }

    return dhct;
}


