    void insert() {
        DonHang model = getForm();
        if (model.getMaDH().isEmpty()) {
            MsgBox.alert(this, "Không được để trống MaDH");

        }
        if (model.getMaGG().isEmpty()) {
            MsgBox.alert(this, "Không được để trống MaGG");
            return;
        }
        try {
            dhdao.insert(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại- Lỗi: Mã đơn hàng đã tồn tại!");
        }
    }

//ĐƠN HÀNG CHI TIẾT
    void insertChiTiet() {
    DonHangCT modelct = getFormct();
    if (modelct != null) {
        try {
            // Giả sử dhctDao là một thể hiện của một lớp tương tác với nguồn dữ liệu
            // Chèn modelct vào nguồn dữ liệu
            dhctDao.insert(modelct);

            // Cập nhật bảng chi tiết sau khi chèn thành công
            this.fillTableChiTiet();

            // Xóa form chi tiết sau khi chèn thành công
            this.clearFormct();

            // Hiển thị thông báo thành công cho người dùng
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            // Xử lý ngoại lệ khi chèn thất bại và hiển thị thông báo lỗi cho người dùng
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }
}


